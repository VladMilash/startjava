public class VariableNamesTheme {
    public static void main(String[] args) {
        
        System.out.println("1. Разные переменные");

        //цифра
        byte number = 1;
        System.out.println(number);

        //сумма чисел
        byte sumNum = 1+1;
        System.out.println(sumNum);

        //произведение цифр
        byte prodNum = 1*3;
        System.out.println(prodNum);

        //максимальное число
        byte maxNum = 127;
        System.out.println(maxNum);

        //количество десятков
        byte numTens = 10/10;
        System.out.println(numTens);

        //вес собаки
        byte weightDog = 10;
        System.out.println(weightDog);

        //исходное число
        byte origNum = 5;
        System.out.println(origNum);

        //процент по вкладу
        byte percDep = 10;
        System.out.println(percDep);

        //(переменная хранит символ) &
        char sym = '&';
        System.out.println(sym);

        //код ощибки
        int errorCode = 999;
        System.out.println(errorCode);

        //тип сообщения
        char messType = 'm';
        System.out.println(messType);

        //число нулей
        int numZeros = 0;
        System.out.println(numZeros);

        //уникальное число
        int uniqueNum = 8965;
        System.out.println(uniqueNum);

        //случайное число
        int ranNum = 323232;
        System.out.println(ranNum);

        //математическое выражение
        int mathExpr = 256+456;
        System.out.println(mathExpr);

        //выбор (чего-либо)
        int choice = 454;
        System.out.println(choice);

        //счет (в игре)
        float score = 3.1f;
        System.out.println(score);

        //максимальная длина (строки)
        Byte maxLength = 100;
        System.out.println(maxLength);

        //пункт меню
        char menuItem = 'R';
        System.out.println(menuItem);

        //стоимость кофе на вынос
        int costCoffeeGo = 200;
        System.out.println(costCoffeeGo);

        //дата начала (чего-либо)
        float starDate = 11.11f;
        System.out.println(starDate);

        //окончание диапазона
        int endRange = 9632;
        System.out.println(endRange);

        //полное имя работника месяца
        char fullNameEmpMonth= 'H';
        System.out.println(fullNameEmpMonth);

        //заголовок электронной книги
        char ebookTitle = 'G';
        System.out.println(ebookTitle);

        //размер
        int size = 666;
        System.out.println(size);

        //вместимость (чего-либо)
        int capacity = 9999;
        System.out.println(capacity);

        //счетчик
        int counter = 55555;
        System.out.println(counter);

        //путь до файла
        char pathToFile = '|';
        System.out.println(pathToFile);

        //количество чисел в каждой строке
        int numOfNumEachLine = 56565;
        System.out.println(numOfNumEachLine);

        System.out.println("\n2. boolean-переменные");

        //сотни равны?
        boolean equals = false;
        System.out.println(equals);

        //компьютер включен?
        boolean working = true;
        System.out.println(working);

        //есть равные цифры?
        boolean theSame = true;
        System.out.println(theSame);

        //(что-либо) создано?
        boolean created = true;
        System.out.println(created);

        //(что-либо) пустое?
        boolean empty = false;
        System.out.println(empty);

        //(что-либо) активное?
        boolean aktive = true;
        System.out.println(aktive);

        //новый?
        boolean new1 = true;
        System.out.println(new1);

        //электронная почта действительная?
        boolean mail = true;
        System.out.println(new1);

        //имеются уникальные строки?
        boolean valid = false;
        System.out.println(valid);

        System.out.println("\n3. Аббревиатуры");

        //старый universally unique identifier
        String cuui = "cuui";
        System.out.println(cuui);

        //производитель оперативной памяти
        String pop ="opp";
        System.out.println(pop);

        //емкость жесткого диска
        String egd = "egd";
        System.out.println(egd);

        //протокол передачи гипертекста
        String ppg = "ppg";
        System.out.println(ppg);

        //сокращенный uniform resource locator
        String curl = "curl";
        System.out.println(curl);

        //новый идентификатор клиента
        String nuk = "nuk";
        System.out.println(nuk);

        //кодировка american standard code for information interchange
        String kascfii = "kascfii";
        System.out.println(kascfii);
    }

}